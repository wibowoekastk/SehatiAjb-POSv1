<script>
    let table, table1;

    $(function () {
        table = $('.table-pembelian').DataTable({
            responsive: true,
            processing: true,
            serverSide: true,
            autoWidth: false,
            ajax: {
                url: '{{ route('pembelian.data') }}',
            },
            columns: [
                {data: 'DT_RowIndex', searchable: false, sortable: false},
                {data: 'tanggal'},
                {data: 'supplier'},
                {data: 'total_item'},
                {data: 'total_harga'},
                {data: 'diskon'},
                {data: 'bayar'},
                {data: 'aksi', searchable: false, sortable: false},
            ]
        });

        // Mengaktifkan kembali inisialisasi tabel supplier
        $('.table-supplier').DataTable();
        // table1 (tabel detail) tetap dinonaktifkan
        // table1 = $('.table-detail').DataTable({ ... });
    });

    function addForm() {
        // Mengaktifkan kembali fungsi untuk menampilkan modal supplier
        $('#modal-supplier').modal('show');
    }

    function showDetail(url) {
        // Fungsi detail tetap dinonaktifkan untuk sementara
        if ($('#modal-detail').length) {
            $('#modal-detail').modal('show');
            table1.ajax.url(url);
            table1.ajax.reload();
        } else {
            alert('Fungsi lihat detail dinonaktifkan untuk sementara.');
        }
    }

    function deleteData(url) {
        if (confirm('Yakin ingin menghapus data terpilih?')) {
            $.post(url, {
                    '_token': $('[name=csrf-token]').attr('content'),
                    '_method': 'delete'
                })
                .done((response) => {
                    table.ajax.reload();
                })
                .fail((errors) => {
                    alert('Tidak dapat menghapus data');
                    return;
                });
        }
    }
</script>
